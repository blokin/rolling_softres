  <h3>What is "Rolling Soft Reserve"?</h3>
  <p>Rolling soft reserve is an unconventional loot method that incentivizes raiders to consistently join our raids by offering bonus points on their soft reserve /roll for returning for multiple consecutive weeks.</p>
  <h3>How does this work?</h3>
  <p>This website pulls data directly from <a href="http://www.softres.it" target="_blank">softres.it</a>'s API.  This data is added to a database, which also keeps track of how many consecutive weeks the item has been registered by the same raider.</p>
  <h3>I'm pretty sure I should have more points..</h3>
  <p>You are more than welcome to head to the <a href="?id=logs">Logs</a> page, which will give you a break down of each and every raid that has been tracked.  These logs are made public for the sake of transparency.  If you believe there is an issue, look at the logs for proof.</p>
